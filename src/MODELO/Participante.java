package MODELO;

/**
 *
 * @author Marcos Luis Goxcon Portillo
 */
public class Participante {
    String nombre;
    String tipo;
    int id;

    public Participante() {
        nombre = "";
        tipo = "";
        id = 0;
        
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    
    
    
}
