    private void btnBuscarAuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarAuActionPerformed
   DefaultTableModel modelo= new DefaultTableModel();
        String audiencia=lblAudiencia.getText();
        try{
       String sql="SELECT NOMBRE,tipo FROM aud_par INNER JOIN audiencia ON audiencia.idAudiencia = aud_par.idAudiencia INNER JOIN participante ON participante.idParticipante = aud_par.idParticipante WHERE aud_par.idAudiencia ='"+audiencia+"'";
        Connection accesoBD = con.conectar();
            PreparedStatement pst = accesoBD.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            ResultSetMetaData rsMd = rs.getMetaData();
            int cantidadcolumnas = rsMd.getColumnCount();
            modelo.addColumn("Nombre");
            modelo.addColumn("Tipo");
            tablaPart.setModel(modelo);
             while(rs.next())    
            {
                Object datos[]=new Object[cantidadcolumnas];
                for(int i =0; i<cantidadcolumnas;i++ )
                {
                    datos[i]=rs.getObject(i+1);
                }
                modelo.addRow(datos);
            }
            rs.close();
   }catch(SQLException e)
   {
       
   }
    }//GEN-LAST:event_btnBuscarAuActionPerformed

